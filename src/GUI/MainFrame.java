    private void jBackMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBackMouseClicked
        // TODO add your handling code here:
        // Go back to previous menu
        jPanel1.setVisible(true);
        if(activeframe==1){
            Flog.setVisible(false);
            if(Flog.isLogg()){
                localGame.setLocal(Flog.getUsername(),Flog.getPass());
                isHost = ClientHTTPS.matchmakingHost(Flog.getUsername(), Flog.getPass());
                jlogin.setText("LOGOUT");
                jlogin.setBackground(new java.awt.Color(51, 255, 51));
            }
        }
        else if(activeframe==2)Fjoin.setVisible(false);
        else if(activeframe==3)Fgame.setVisible(false);
        else if(activeframe==4)Finstruct.setVisible(false);
        else if(activeframe==5) Freg.setVisible(false);
    }//GEN-LAST:event_jBackMouseClicked

