    /**
     * Read file and set sentence
     * @return 
     * @throws java.io.FileNotFoundException 
     */
    public String getSetence() throws FileNotFoundException, IOException{
        Random r = new Random();
        int R = r.nextInt(dictionaryLenght)+1;
        String sentence= "duh";
        BufferedReader br;
        br = new BufferedReader(new FileReader("dictionary.txt"));
        for(int i=0;i<R;i++){
            sentence = br.readLine();
        }
        return sentence;
    }
